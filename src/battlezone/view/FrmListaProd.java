    public void calcular(){
        String pre;
        String quan;
        double total = 0;
        double preco;
        int quantidade;
        double  calc = 0.0;
        
        for(int i = 0; i < FrmCaixa.tabela.getRowCount(); i++){ // percorrer todos os items do formulário vendas para verificar as quantidades e ir totalizando
            pre = FrmCaixa.tabela.getValueAt(i, 3).toString(); // recebe o preço do formulário Caixa, no campo o campo 4
            quan = FrmCaixa.tabela.getValueAt(i, 4).toString(); // recebe a quantidade do formulário Caixa
            preco = Double.parseDouble(pre); // guardará os valores do campo pre convertendo para Double
            quantidade = Integer.parseInt(quan); // guardará a quantidade do campo quan sendo todos inteiros
            calc = preco * quantidade; // faz o cáculo 
            total = total + calc; // faz o cálculo do total referente a quantidade que for adquirida
            FrmCaixa.tabela.setValueAt(Math.rint(calc * 100) / 100, i, 5); // está passando um valor na possição 5, ou seja, no TOTAL. E o cáculo é p/ dar uma quebra nos números, como 0,00.
        }
        FrmCaixa.total.setText("" + Math.rint(total * 100) / 100);// pegará o texto e mostrará de forma arredondada (Math.rint)
    }

