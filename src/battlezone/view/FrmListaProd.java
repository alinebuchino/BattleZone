    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        if (tabela.getRowCount() > 0){ // verifica se a tabela há algum registro
            try{
                String quant = null;
                DefaultTableModel tabeladet = (DefaultTableModel) FrmCaixa.tabela.getModel(); // referencia a tabela do formulário caixa criando o objeto tabeladet
                
                String[] dado = new String[6]; // armazena as informações dos 6 campos
                
                int linha = tabela.getSelectedRow(); // variável linha para verificar cada linha selecionada
                
                if(linha == -1){ // se não tiver linha selecionada
                    JOptionPane.showMessageDialog(this, "Selecione um produto", "Produtos", 0,
                    new ImageIcon (getClass().getResource("/imagens/principal/info.png")));
                }else{
                    String cod = tabela.getValueAt(linha, 0).toString(); // recebe o que tiver na 1° posição da tabela
                    String tipo = tabela.getValueAt(linha, 1).toString();
                    String nom = tabela.getValueAt(linha, 2).toString();
                    String preco = tabela.getValueAt(linha, 3).toString();
                    int c = 0; // irá passar as informações e valores
                    int j = 0;
                    
                    // o showInputDialog aceita o recebimento de valores
                    
                    quant = JOptionPane.showInputDialog(this, "Quantidade", "Produtos", JOptionPane.INFORMATION_MESSAGE); //mostrará a quantidade dos produtos
                    while (!ListaProd_CRUD.isNumber(quant) && quant != null){ // Verifica se não está passando um valor numérico ou se o valor numérico é nulo
                        quant = JOptionPane.showInputDialog(this, "Somente valores numéricos maiores que 0:",
                                JOptionPane.ERROR_MESSAGE);
                    }
                    if((quant.equals("")) || (quant.equals("0"))){
                        JOptionPane.showMessageDialog(this, "Insira um valor maior que 0", "Produtos", 0,
                        new ImageIcon (getClass().getResource("/imagens/principal/info.png")));
                    }else{
                        for (int i = 0; i < FrmCaixa.tabela.getRowCount(); i++){
                            Object com = FrmCaixa.tabela.getValueAt(i, 0); // Guarda as informações da 1° posição (código)
                            Object quant1 = FrmCaixa.tabela.getValueAt(i, 4); // guarda as informações da 4° posição (quantidade)
                            if(cod.equals(com)){ // verifica se o código do FrmListaProd é o mesmo que foi enviado ao FrmCaixa
                                j = i; // servirá como a posição dos objetos na tabela
                                int quantT = Integer.parseInt(quant) + Integer.parseInt((String) quant1); // pega a quant anterior (que já havia add) e soma com a nova (que está add no momento)
                                FrmCaixa.tabela.setValueAt(String.valueOf(quantT), i, 4); // aplica a quantidade total no campo 4
                                c++; // irá incrementar para passar pro próximo
                                calcular(); // efetuará o cálculo no método que foi criado
                                FrmCaixa.recebido.setText(""); // os campos são zerados p/ digitar o valor recebido e verificar o calculo que será feito
                                FrmCaixa.troco.setText("");
                            }
                        }
                        if (c == 0){ // o contador irá receber valores de acordo com as posições
                            
                            dado[0] = cod;
                            dado[1] = tipo;
                            dado[2] = nom;
                            dado[3] = preco;
                            dado[4] = quant;
                            
                            tabeladet.addRow(dado); // irá ser adicionado na tabela referente a tabela Caixa
                            
                            FrmCaixa.tabela.setModel(tabeladet); // passará as informações para a tabela
                            calcular(); // irá recalcular
                            
                            FrmCaixa.recebido.setText(""); // limpar novamente os campos
                            FrmCaixa.troco.setText("");
                        }
                    }
                }
            }catch (Exception e){ 
            }
        }else{
            JOptionPane.showMessageDialog(this, "Não há registros", "Produtos", 0,
            new ImageIcon (getClass().getResource("/imagens/principal/info.png")));
        }
    }//GEN-LAST:event_registrarActionPerformed


